CREATE OR REPLACE  FUNCTION "F_SL_DAT_LOP"    (
V_MALOP VARCHAR,
V_MAHK VARCHAR
)
RETURN NUMBER
AS
V_SL_DAT NUMBER(9,2);
BEGIN
  SELECT COUNT(*) INTO V_SL_DAT
  FROM HOCSINH
  WHERE F_SOLUONG_DAT_MON(MAHS,V_MAHK)=9 AND MALOP=V_MALOP;
RETURN V_SL_DAT;
END;

/

CREATE OR REPLACE  FUNCTION "F_SL_DAT_MON"     (
v_mamon varchar,
v_malop varchar,
v_mahk varchar
)
return number
as
V_sl number(9,2);
begin
  select count(*) into V_sl
  from diemmon,hocsinh
  where mamonhoc=v_mamon and malop=v_malop and mahocky=v_mahk and mahs=mahocsinh and trungbinh>=5;
return V_sl;
end;
/
CREATE OR REPLACE  FUNCTION "F_SOLUONG_DAT_MON"   (
V_MAHS VARCHAR,
V_MAHK VARCHAR
)
RETURN  NUMBER
IS
 V_SL NUMBER(9,2);
BEGIN
select count(*) INTO V_SL
from v_dshs_lop
where mahocky=V_MAHK and maHS=V_MAHS and trungbinh>=5.0;
RETURN V_SL;
END;

