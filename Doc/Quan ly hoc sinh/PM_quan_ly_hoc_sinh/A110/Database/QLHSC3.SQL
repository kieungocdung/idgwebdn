CREATE OR REPLACE  FUNCTION "F_TINH_DTB_HOCSINH_MON" 
(
V_MAHOCSINH VARCHAR,
V_MAMON VARCHAR
)
RETURN NUMBER
IS
DTB NUMBER(9,2);
BEGIN
  SELECT TRUNGBINH INTO DTB
  FROM DIEMMON
  WHERE MAMONHOC=V_MAMON AND MAHOCSINH=V_MAHOCSINH ;
  RETURN DTB;
END;

